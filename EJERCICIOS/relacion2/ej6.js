function ej6(value){
    
    if (value=="amarillo"){
        document.body.setAttribute("bgColor", "#F1f14e");
    }else if(value=="rosa"){
        document.body.setAttribute("bgColor", "pink");
    }else if(value=="aqua"){
        document.body.setAttribute("bgColor", "aquamarine");
    }else if(value=="verde"){
        document.body.setAttribute("bgColor", "green");
    }else if(value=="azul"){
        document.body.setAttribute("bgColor", "blue");
    }
}